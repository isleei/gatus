const normalizeLocale = (value) => {
  if (!value || typeof value !== 'string') return DEFAULT_LOCALE
  const lower = value.toLowerCase()
  if (lower.startsWith('zh')) return 'zh-CN'
  if (SUPPORTED_LOCALES.includes(value)) return value
  if (lower.startsWith('en')) return 'en-US'
  return DEFAULT_LOCALE
}

const readInitialLocale = () => {
  if (typeof window === 'undefined') return DEFAULT_LOCALE
  const stored = window.localStorage.getItem(STORAGE_KEY)
  if (stored) return normalizeLocale(stored)
  return normalizeLocale(window.navigator.language)
}

const currentLocale = ref(readInitialLocale())

const getMessage = (localeCode, key) => {
  const segments = key.split('.')
  let value = messages[localeCode]
  for (const segment of segments) {
    if (value && typeof value === 'object' && segment in value) {
      value = value[segment]
    } else {
      return undefined
    }
  }
  return value
}

const interpolate = (template, params = {}) => {
  return template.replace(/\{(\w+)\}/g, (_, key) => {
    if (Object.prototype.hasOwnProperty.call(params, key)) {
      return String(params[key])
    }
    return `{${key}}`
  })
}

export const translate = (key, params = {}) => {
  const localizedMessage = getMessage(currentLocale.value, key) ?? getMessage(DEFAULT_LOCALE, key)
  if (typeof localizedMessage === 'function') {
    return localizedMessage(params)
  }
  if (typeof localizedMessage === 'string') {
    return interpolate(localizedMessage, params)
  }
  return key
}

export const getCurrentLocale = () => currentLocale.value

export const setLocale = (localeCode) => {
  const normalized = normalizeLocale(localeCode)
  currentLocale.value = normalized
  if (typeof window !== 'undefined') {
    window.localStorage.setItem(STORAGE_KEY, normalized)
    document.documentElement.lang = normalized
  }
}

export const initI18n = () => {
  setLocale(currentLocale.value)
}

export const useI18n = () => ({
  locale: computed(() => currentLocale.value),
  setLocale,
  t: translate,
  availableLocales: SUPPORTED_LOCALES,
})
